package org.opensaga.plugin.builder.meta.generator;

/**
 * Main exception for all purposes whenever the generation of the meta classes
 * failed.
 * 
 * @author cklewes
 */
public class MetaGenerationFailedException
    extends Exception
{
    private static final long serialVersionUID = -8893831880642697184L;

    public MetaGenerationFailedException(String message, Throwable cause)
    {
        super(message, cause);
    }


    public MetaGenerationFailedException(String message)
    {
        super(message);
    }
}
